import UIKit

extension UIColor {
    struct Theme {
        static var blue: UIColor { return UIColor(red: 0.66, green: 0.75, blue: 1, alpha: 1) }
        static var green: UIColor { return UIColor(red: 0.78, green: 1, blue: 0.68, alpha: 1) }
        static var orange: UIColor { return UIColor(red: 1, green: 0.82, blue: 0.69, alpha: 1) }
        static var yellow: UIColor { return UIColor(red: 0.96, green: 0.95, blue: 0.69, alpha: 1) }
        static var red: UIColor { return UIColor(red: 1, green: 0.65, blue: 0.65, alpha: 1) }
        static var lightGray: UIColor { return UIColor(red: 0.98, green: 0.98, blue: 0.98, alpha: 1) }
    }
}
